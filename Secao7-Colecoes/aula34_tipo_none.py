"""
Tipo None

O tipo de dado None em Python representa o tipo sem tipo, ou poderia ser conhecido também
como tipo vazio, porem, falar que é um tipo sem tipo é mais apropriado.

OBS: O tipo Nome é SEMPRE especificado com a primeira letra maiúscla.

Certo: None
Errado: none

Qunado utilizamos?

- Podemos utilizar None quando queremos criar uma variável e inicializa-la com um tipo sem tipo, antes
de recebermos um valor final.

OBS: O tipo None em Python é SEMPRE considerado como falso
"""

numeros = None

print(numeros)
print(type(numeros))

numeros = 1.44, 1.34, 5.67

print(numeros)
print(type(numeros))